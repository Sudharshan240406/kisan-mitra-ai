class TelephonyProviderRegistry:
    """
    Registry supervising loading, versioning, health, and registry tracking of telephony providers.
    """
    def __init__(self, event_bus: Optional[Any] = None, governance_engine: Optional[Any] = None) -> None:
        self._providers: dict[str, ITelephonyProvider] = {}
        self._event_bus = event_bus
        self._governance_engine = governance_engine

    def register(self, provider: ITelephonyProvider) -> None:
        """Registers a telephony provider into the MIP."""
        self._providers[provider.id] = provider
        logger.info(f"Registered Telephony Provider '{provider.id}' v{provider.version}.")

        # Auto-register with Governance Engine if present
        if self._governance_engine:
            try:
                self._governance_engine.register_artifact(
                    artifact_type="telephony_provider",
                    artifact_id=provider.id,
                    version=provider.version,
                    status=provider.status.value
                )
            except Exception as e:
                logger.error(f"Failed to register provider '{provider.id}' in governance ledger: {e}")

    def deregister(self, provider_id: str) -> None:
        """Deregisters a telephony provider."""
        if provider_id in self._providers:
            del self._providers[provider_id]
            logger.info(f"Deregistered Telephony Provider '{provider_id}'.")

    def discover(self, provider_id: str) -> Optional[ITelephonyProvider]:
        """Looks up a telephony provider by ID."""
        return self._providers.get(provider_id)

    def list_providers(self) -> list[ITelephonyProvider]:
        """Lists all registered telephony providers."""
        return list(self._providers.values())

    def validate_dependencies(self, provider_id: str) -> bool:
        """Validates that a telephony provider has its configured properties."""
        provider = self.discover(provider_id)
        if not provider:
            return False
        if not provider.id or not provider.version or not provider.status:
            return False
        return True

    def load_from_config(self, configs: list[dict[str, Any]]) -> None:
        """Dynamically instantiates and registers mock providers from a configuration array."""
        for cfg in configs:
            try:
                pid = cfg.get("provider_id")
                ptype = cfg.get("provider_type")
                version = cfg.get("version", "1.0.0")
                caps = cfg.get("capabilities", [])

                if not pid or not ptype:
                    continue

                provider: ITelephonyProvider

                if ptype == "twilio":
                    provider = TwilioTelephonyProvider(pid, version, caps)
                elif ptype == "plivo":
                    provider = PlivoTelephonyProvider(pid, version, caps)
                elif ptype == "exotel":
                    provider = ExotelTelephonyProvider(pid, version, caps)
                elif ptype == "bsnl":
                    provider = BSNLTelephonyProvider(pid, version, caps)
                else:
                    provider = BaseTelephonyProvider(pid, version, caps)

                self.register(provider)
            except Exception as e:
                logger.error(f"Failed to load telephony provider from configuration {cfg}: {e}")

    async def health_check(self) -> dict[str, Any]:
        """Queries health check across all registered providers."""
        report: dict[str, Any] = {}
        for pid, provider in self._providers.items():
            try:
                is_healthy = await provider.health_check()
                report[pid] = {
                    "version": provider.version,
                    "status": provider.status.value,
                    "healthy": is_healthy,
                    "latency_ms": provider.latency_ms
                }
            except Exception as e:
                report[pid] = {"healthy": False, "error": str(e)}
        return report
